    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        // 削除されていないtodoのみ取得
        $fetch_todos = Todo::where('deleted',false)->get();
        return view('index',compact('fetch_todos'));
    }

